package wse.utils.exception;

public class WseSHttpInitException extends WseException {
	private static final long serialVersionUID = 486529171433542386L;
	
	public WseSHttpInitException() {
		super();
	}
	
	public WseSHttpInitException(String message)
	{
		super(message);
	}
	
	public WseSHttpInitException(Throwable cause)
	{
		super(cause);
	}

	public WseSHttpInitException(String message, Throwable cause) {
		super(message, cause);
	}

	public WseSHttpInitException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	
	
	

}
